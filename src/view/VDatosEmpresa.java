    private void cargarTextField() throws SQLException, ClassNotFoundException {
        FnInfo load = new FnInfo();
        Info datos = load.cargar(1);
        if(datos != null){
            if(datos.getCelular() != null)
                txtCelular.setText(datos.getCelular());
            if(datos.getCiudad()!= null)
                txtCiudad.setText(datos.getCiudad());
            if(datos.getDireccion()!= null)
                txtDireccion.setText(datos.getDireccion());
            
            if(datos.getMail()!= null)
                txtMail.setText(datos.getMail());
            
            if(datos.getNombre()!= null)
                txtNombre.setText(datos.getNombre());
            
            if(datos.getTelefono()!= null)
                txtTelefono.setText(datos.getTelefono());
            
            if(datos.getWeb()!= null)
                txtWeb.setText(datos.getWeb());
            
        }
    }
