    private String getIdFromString(String arg){
        arg = GV.getStr(arg);
        if(arg.contains("<") && !arg.endsWith("<")){
            arg=arg.substring(arg.indexOf("<")+1).replaceAll(">", "");
            return arg;
        }
        return "0";
    }
     
