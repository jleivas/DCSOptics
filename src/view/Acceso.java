    private void entrar(){
        String user = txtUser.getText();
        String pass = txtPass.getText();
        FnUser upUser = new FnUser();
        User usu;
        try{
            usu=upUser.validar(user,pass);
            if(usu!=null){
                        this.setVisible(false);
                        try{
                            GlobalValues.USERNAME = usu.getNombre();
                            GlobalValues.ID_USER = usu.getId();
                            ContentAdmin principalAdmin = new ContentAdmin();
                            principalAdmin.setVisible(true);
//                            load.setStaticUser(user);
                            this.dispose();
                        }catch(ClassNotFoundException | SQLException ex){
                            JOptionPane.showMessageDialog(null, "Error. No se pueden cargar los datos."+ex.getMessage(),"Datos de acceso",JOptionPane.WARNING_MESSAGE);
                        }

            }else{
                JOptionPane.showMessageDialog(null, "Datos de acceso incorrectos","Datos de acceso",JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error. No se pueden cargar los datos.\nEs probable que el servidor no se haya iniciado.","Datos de acceso",JOptionPane.WARNING_MESSAGE);
        }
    }
