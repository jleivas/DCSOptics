    private void autocompletar() throws SQLException, ClassNotFoundException {
        Dao load = new Dao();
        TextAutoCompleter textAutoCompleter2 = new TextAutoCompleter(txtNombreInstitucion);
        for (String temp : load.listarLugarEntrega()) {
            if(!temp.isEmpty()){
                textAutoCompleter2.addItem(temp);
                textAutoCompleter2.setMode(0);
            }
        }
    }

