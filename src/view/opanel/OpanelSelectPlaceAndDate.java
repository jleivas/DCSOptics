    private void btnLoadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoadMouseClicked
        String name = txtNombreInstitucion.getText();
        GV.cursorWAIT(this);
        if(!GV.getStr(name).isEmpty()){
            GV.setLugarEntregaFiltrar(name);
            if(txtFecha1 == null && txtFecha2 == null){
                GV.cursorDF(this);
                OptionPane.showMsg("Falta información", "Debe seleccionar al menos una fecha válida", 2);
                return;
            }
            if(txtFecha1.getDate() == null){
                if(txtFecha2.getDate() == null){
                    GV.setDateFrom(new Date());
                    GV.setDateTo(new Date());
                }else{
                    GV.setDateFrom(txtFecha2.getDate());
                    GV.setDateTo(txtFecha2.getDate());
                }
            }else{
                if(txtFecha2.getDate() == null){
                    GV.setDateFrom(txtFecha1.getDate());
                    GV.setDateTo(txtFecha1.getDate());
                }else{
                    GV.setDateFrom(txtFecha1.getDate());
                    GV.setDateTo(txtFecha2.getDate());
                }
            }
            OptionPane.closeOptionPanel();
            try {
                boton.fichas(GV.getCboFichasFilterData());
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(OpanelSelectPlaceAndDate.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            GV.cursorDF(this);
            OptionPane.showMsg("Falta ingresar un valor", "Debe seleccionar un ligar de entrega para filtrar", 2);
        }
    }//GEN-LAST:event_btnLoadMouseClicked

