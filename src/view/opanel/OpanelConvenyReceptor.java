    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        
        String name = txtName.getText();
        String dir = txtDir.getText();
        String ct1 = txtCt1.getText();
        String ct2 = txtCt2.getText();
        GlobalValuesVariables.setRececptor(GV.getToName(name), GV.getToName(dir), ct1, ct2);
        if(name.isEmpty() && dir.isEmpty() && ct1.isEmpty() && ct2.isEmpty()){
            if(OptionPane.getConfirmation("Generar reporte", "No ha ingresado datos para el receptor,\n"
                    + "¿Desea continuar de todas formas?", 2)){
                GV.printConvenio(convenio);
                OptionPane.closeOptionPanel();
            }else{
                return;
            }
        }else{
            String resumen = getResumenDatosReceptor(GV.getToName(name),GV.getToName(dir),ct1,ct2);
            if(OptionPane.getConfirmation("Confirmar datos", resumen+"\n"
                        + "¿Confirmas que los datos son correctos?", 2)){
                GV.printConvenio(convenio);
                OptionPane.closeOptionPanel();
            }
        }
        return;
    }//GEN-LAST:event_btnGuardarMouseClicked

