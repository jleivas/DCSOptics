    private String getResumenDatosReceptor(String name, String dir, String ct1, String ct2) {
        String resumen = (GV.getStr(name).isEmpty())?"":"Nombre: "+name;
        resumen = (GV.getStr(dir).isEmpty())?resumen+"":resumen+", Dirección: "+dir;
        resumen = (GV.getStr(ct1).isEmpty())?resumen+"":resumen+", \nDatos de contacto: "+ct1;
        resumen = (GV.getStr(ct2).isEmpty())?resumen+".":resumen+", "+ct2;
        return (resumen.startsWith(", "))?resumen.replaceFirst(", ", ""):resumen;
    }

