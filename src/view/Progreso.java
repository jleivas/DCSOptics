    public void setLista(ArrayList<Ficha> lista, Info empresa, int funcion) throws InterruptedException {
        
        
        centrarPantalla();
        cantidad = lista.size();
        //Creamos un Thread para mejorar el ejemplo
        final Thread t;
        //Inicializamos
        t = new Thread(new Runnable() {
            //Implementamos el método run()
            @Override
            public void run() {
                //Permite mostrar el valor del progreso
                BarraProgreso.setStringPainted(true);
                x = 0;
                //Utilizamos un while para emular el valor mínimo y máximo
                //En este caso 0 - 100
                while(x <= 100){
                    
                    //Asignamos valor a nuestro JProgressBar por cada siclo del bucle
                    BarraProgreso.setValue(x);
                    //Valor que se mostrará en el JTextArea
                    
                    x = (100*(i+1))/cantidad;
                    
                    try {
                        if(i < cantidad){
                            if(funcion == 0){
                                if(!enviarDatosFicha(lista.get(i), empresa)){
                                    JOptionPane.showMessageDialog(null, "Error al enviar comprobante a: "+lista.get(i).getCliente().getNombre()+" de rut: "+lista.get(i).getCliente().getRut());  
                                    txtInfo.append("Error al enviar Comprobante a " + lista.get(i).getCliente().getNombre() + "\n");
                                    txtInfo.append("Correo " + lista.get(i).getCliente().getEmail()+ "...\n\n");
                                }else{
                                    txtInfo.append("Comprobante enviado a " + lista.get(i).getCliente().getNombre() + "\n");
                                    txtInfo.append("Correo " + lista.get(i).getCliente().getEmail()+ "...\n\n");
                                }
                                
                            }else{
                                if(funcion == 1){
                                    if(!enviarNotificacionFicha(lista.get(i), empresa)){
                                        JOptionPane.showMessageDialog(null, "Error al enviar Notificación a: "+lista.get(i).getCliente().getNombre()+" de rut: "+lista.get(i).getCliente().getRut());
                                        txtInfo.append("Error al enviar Notificación a " + lista.get(i).getCliente().getNombre() + "\n");
                                        txtInfo.append("Correo " + lista.get(i).getCliente().getEmail()+ "...\n\n");
                                    }else{
                                        txtInfo.append("Notificación enviada a " + lista.get(i).getCliente().getNombre() + "\n");
                                        txtInfo.append("Correo " + lista.get(i).getCliente().getEmail()+ "...\n\n");
                                    }
                                    
                                }
                            }
                            
                        }
                        
                    } catch (Exception e) {
                    }
//                    if(i < cantidad){
//                        txtInfo.append("Comprobante enviado a " + lista.get(i).getCliente().getNombre() + "\n");
//                        txtInfo.append("Correo " + lista.get(i).getCliente().getEmail()+ "...\n\n");
//                    }
                        
                    i++;
                }
                try {
                    Thread.sleep(2000);
                    
                } catch (InterruptedException ex) {
                    Logger.getLogger(Progreso.class.getName()).log(Level.SEVERE, null, ex);
                }
                Progreso.this.setVisible(false);
            }
        });
        //Se ejecuta el Thread
        t.start();
        
    }
    
