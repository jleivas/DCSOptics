    private void cargarDatos(String listar) {
        limpiarTextField();
        if(listar.equals("eliminados")){
            btnRestaurar.setVisible(true);
            btnEliminar.setVisible(false);
            btnAbrir.setVisible(false);
            btnModificar.setVisible(false);
            btnBuscar.setVisible(false);
            btnBuscarEliminados.setVisible(true);
        }else{
            btnRestaurar.setVisible(false);
            btnEliminar.setVisible(true);
            btnAbrir.setVisible(true);
            btnModificar.setVisible(true);
            btnBuscar.setVisible(true);
            btnBuscarEliminados.setVisible(false);
        }
            
        txtRut.setEditable(false);
        DefaultTableModel modelo;
        modelo = new DefaultTableModel() {
           @Override
           public boolean isCellEditable(int fila, int columna) {
               return false; //Con esto conseguimos que la tabla no se pueda editar
           }
        };
        
        modelo.addColumn("Rut");
        modelo.addColumn("Nombre");
        modelo.addColumn("Email");
        modelo.addColumn("Teléfono");
        tblClientes.setModel(modelo);
        FnDoctor datbd= new FnDoctor();
        int cont =0;
        try{
            for (Doctor temp : datbd.listar(listar)) {
                Object[] fila = new Object[4];
                fila[0] = temp.getRut();
                fila[1] = temp.getNombre();
                fila[2] = temp.getEmail();
                fila[3] = temp.getTelefono();
                modelo.addRow(fila);
            }
            tblClientes.updateUI();
            if(tblClientes.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "No existen doctores registrados.");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error, ["+e.getMessage()+"]");
        }
    }

    private void abrirCliente(String rut) throws SQLException, ClassNotFoundException {
            FnDoctor load = new FnDoctor();
            Doctor temp = load.cargar(rut);
            if(temp!=null){
                txtRut.setText(temp.getRut());
                if(temp.getNombre().equals("null"))
                    txtNombre.setText("");
                else
                    txtNombre.setText(temp.getNombre());
                if(temp.getTelefono().equals("null"))
                    txtTelefono.setText("");
                else
                    txtTelefono.setText(temp.getTelefono());
                if(temp.getEmail().equals("null"))
                    txtEmail.setText("");
                else
                    txtEmail.setText(temp.getEmail());
                
                
            }else{
                JOptionPane.showMessageDialog(null, "Error al cargar el doctor", "Seleccione Doctor", JOptionPane.INFORMATION_MESSAGE);
            }
    }

    private void buscarDatos(String buscar, int i) {
        DefaultTableModel modelo;
        modelo = new DefaultTableModel() {
           @Override
           public boolean isCellEditable(int fila, int columna) {
               return false; //Con esto conseguimos que la tabla no se pueda editar
           }
        };
        
        modelo.addColumn("Rut");
        modelo.addColumn("Nombre");
        modelo.addColumn("Email");
        modelo.addColumn("Teléfono");
        tblClientes.setModel(modelo);
        FnDoctor datbd= new FnDoctor();
        int cont =0;
        try{
            for (Doctor temp : datbd.buscar(buscar,i)) {
                Object[] fila = new Object[4];
                fila[0] = temp.getRut();
                fila[1] = temp.getNombre();
                fila[2] = temp.getEmail();
                fila[3] = temp.getTelefono();
                modelo.addRow(fila);
            }
            tblClientes.updateUI();
            if(tblClientes.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "No se encontraron resultados, pruebe con otros valores de búsqueda.");
                if(i == 1){
                    cargarDatos("");
                }else{
                    cargarDatos("eliminados");
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error, ["+e.getMessage()+"]");
        }
    }

