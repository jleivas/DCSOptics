            String nombre=txtNombreNew.getText();
            String telefono=txtTelefonoNew.getText();
            String mail=txtEmailNew.getText();
            int estado=1;

            Doctor doctor= new Doctor(rut, nombre, telefono, mail, estado);
            FnDoctor fn = new FnDoctor();
