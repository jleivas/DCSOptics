    private void cargarDatos(int idFolio) throws SQLException, ClassNotFoundException {
       
        DefaultTableModel modelo;
        modelo = new DefaultTableModel() {
           @Override
           public boolean isCellEditable(int fila, int columna) {
               return false; //Con esto conseguimos que la tabla no se pueda editar
           }
        };
        
        modelo.addColumn("Fecha");
        modelo.addColumn("Abono");
        modelo.addColumn("Tipo de Pago");
        tblListar.setModel(modelo);
        FnFicha load = new FnFicha(); 
        Ficha ficha = load.cargar(idFolio, 1);
        lblDetalleDespacho.setText(load.obtenerNombreDespacho(idFolio));
        lblTitulo.setText("Ficha numero: "+ficha.getId());
        lblCliente.setText("Cliente: "+ficha.getCliente().getNombre());
        TipoPago tp = null;
        int cont =0;
        try{
            for (HistorialPago temp : load.listarHistorial(idFolio)) {
                Object[] fila = new Object[3];
                fila[0] = temp.getFecha().toString();
                fila[1] = "$ "+temp.getAbono();
                tp = load.cargarTipoPago(temp.getIdTipoPago());
                if(tp!=null)
                    fila[2] = tp.getNombre();
                else
                    fila[2] = "No registrado.";
                modelo.addRow(fila);
            }
            tblListar.updateUI();
            if(tblListar.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "No se encontraron resultados, pruebe con otros valores de búsqueda.");
                volver();
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error, ["+e.getMessage()+"]");
        }
    }

    public void setIdFolio(int idFicha) throws SQLException, ClassNotFoundException {
        this.idFolio.setText(""+idFicha);
        cargarDatos(idFicha);
    }

    private void volver() throws SQLException, ClassNotFoundException {
        Boton btn = new Boton();
        btn.abrirFicha(idFolio.getText());
    }
    
