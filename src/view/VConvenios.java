            int montoMax = 0;
            int montoPp = 0;
            int cantClientes = 0;
            int idDescuento  =  0;
            String idInstitucion = getIdInstitucion(txtInstitucionN.getText());
            int porcAdicional = (int)txtPorcentajeAdicionalN.getValue();
