    private String getIdInstitucion(String text) {
        if(text!=null){
            text = ((text.startsWith("[")) && (text.contains("]")))?text.substring(1, text.indexOf("]")):null;
            text = (!(GV.getFilterString(text).isEmpty()))?text:null;
        }
        return text;
    }
