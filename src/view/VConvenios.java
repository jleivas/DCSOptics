    private void load(){
        if(cboMostrar.getSelectedIndex()==0){//en nueva version cargar ventana completa con lista de clientes estatica global
            cargarDatos("0");
        }else{
            cargarDatos("-1");
        }
    }
    private void cargarDatos(String listar) {
        limpiarTextField();
        loadPanels(1);
        listConvenios = load.listar(listar, new Convenio());
        if(listar.equals("-1")){
            btnRestaurar.setVisible(true);
            btnEliminar.setVisible(false);
            btnAbrir.setVisible(false);
            btnModificar.setVisible(false);
        }else{
            btnRestaurar.setVisible(false);
            btnEliminar.setVisible(true);
            btnAbrir.setVisible(true);
            btnModificar.setVisible(true);
        }
        try{
            modelo.setNumRows(0);
            for (Object object : listConvenios) {
                Convenio temp = (Convenio)object;
                GV.convenioUpdateBDIfValidated(temp);
                Institucion ins = (Institucion)load.get(temp.getIdInstitucion(), 0, new Institucion());
                String insName = (ins != null)?((Institucion)ins).getNombre():"No asignada";
                String estado = (temp.getEstado() == 1)?"Activo":"Generado";
                estado=(temp.getEstado()==3)?"Pagado":estado;
                Object[] fila = new Object[5];
                fila[0] = temp.getId();
                fila[1] = temp.getNombre();
                fila[2] = insName;
                fila[3] = (listar.equals("-1"))?"Anulado":estado;
                modelo.addRow(fila);
            }
            tblListar.updateUI();
            if(tblListar.getRowCount() == 0){
                GV.emptyTable(cboMostrar, txtBuscar, "Convenios");
            }
            
        }catch(Exception e){
            OptionPane.showMsg("Ocurrió un error inesperado", "Ocurrió un error inesperado al cargar valores en la tabla, ["+e.getMessage()+"]",3);
        }
    }

    private void abrirConvenio(int id) throws SQLException, ClassNotFoundException, InstantiationException, IllegalAccessException {
        loadPanels(2);
        stConvenio = (Convenio)GV.buscarPorIdEnLista(""+id, listConvenios, new Convenio());
            if(stConvenio!=null){
                if(stConvenio.getEstado() > 1){
                    setBtnVisible(true);
                }else{
                    setBtnVisible(false);
                }
                if(stConvenio.getEstado() == 3){
                    btnPagarCuotas.setVisible(false);
                }
                if(stConvenio.getNombre().isEmpty() || stConvenio.getNombre().equals("null"))
                    txtNombreU.setText("");
                else
                    txtNombreU.setText(stConvenio.getNombre());
                txtFechaIniU.setDate(stConvenio.getFechaInicio());
                txtFechaFinU.setDate(stConvenio.getFechaFin());
                txtFechaCobroU.setDate(stConvenio.getFechaCobro());
                txtPorcentajeAdicionalU.setValue((int)stConvenio.getPorcentajeAdicion());
                txtCuotasU.setValue(stConvenio.getCuotas());
                if(stConvenio.getIdInstitucion().isEmpty())
                    txtInstitucionU.setText("");
                else
                    txtInstitucionU.setText("["+stConvenio.getIdInstitucion()+"] "+((Institucion)load.get(stConvenio.getIdInstitucion(), 0, new Institucion())).getNombre());
                
            }else{
                OptionPane.showMsg("Seleccione registro","Error al cargar valores,\n"
                    + "es posible que no se haya seleccionado un registro\n"
                    + "o el valor seleccionado no tiene un identificador válido.",2);
            }
    }

    private void limpiarTextField() {
        txtNombreU.setText("");
        txtNombreN.setText("");
        txtCuotasN.setValue((int)1);
        txtCuotasU.setValue((int)1);
        txtFechaFinU.setDate(null);
        txtFechaIniN.setDate(null);
        txtFechaCobroN.setDate(null);
        txtFechaIniU.setDate(null);
        txtFechaTerN.setDate(null);
        txtFechaCobroU.setDate(null);
        txtInstitucionN.setText("");
        txtInstitucionU.setText("");
    }

    private void autocompletar() throws SQLException, ClassNotFoundException {
        TextAutoCompleter textAutoCompleter1 = new TextAutoCompleter(txtInstitucionN);
        TextAutoCompleter textAutoCompleter2 = new TextAutoCompleter(txtInstitucionU);
        
        textAutoCompleter1.setMode(0);
        textAutoCompleter2.setMode(0);
        for (Object object : load.listar("0", new Institucion())) {
            Institucion temp = (Institucion)object;
            
            textAutoCompleter1.addItem("["+temp.getCod()+"] "+temp.getNombre());
            textAutoCompleter1.setMode(0);
            
            textAutoCompleter2.addItem("["+temp.getCod()+"] "+temp.getNombre());
            textAutoCompleter2.setMode(0);
        }
    }
    
