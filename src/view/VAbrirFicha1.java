    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed

        int abono = (int) txtAbono.getValue();
        if(abono <= 0){
            JOptionPane.showMessageDialog(null, "Abono ingresado debe ser mayor a cero","Error de datos",JOptionPane.WARNING_MESSAGE);
            return;
        }
        Date fecha = txtFecha1.getDate();
        calcularSaldo();
        if(fecha == null){
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fecha","Faltan datos",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(abono > 0 && cboTipoPago.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Debe registrar un tipo de abono", "Tipo de pago no seleccionado", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        FnFicha load = new FnFicha();

        int idFolio = 0;

        idFolio = Integer.parseInt(txtFolioPagar.getText());
        
        Ficha ficha = null;
        try {
            ficha = load.cargar(idFolio,1);
            if(fecha.compareTo(ficha.getFecha()) < 0 || fecha.compareTo(new Date()) > 0){
                JOptionPane.showMessageDialog(null, "Ingrese una fecha válida","Error",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if(ficha == null){
                JOptionPane.showMessageDialog(null, "Error al cargar ficha desde la base de datos","Error al cargar ficha",JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error desconocido:"+ex.getMessage(),"Error al cargar ficha",JOptionPane.WARNING_MESSAGE);
            return;
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Error desconocido:"+ex.getMessage(),"Error al cargar ficha",JOptionPane.WARNING_MESSAGE);
            return;
        }
        HistorialPago hp = null;

        int saldo = 0;
        try {
            saldo = load.cargar(idFolio,1).getSaldo();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error inesperado: ["+ex.getMessage()+"]","Error al cargar ficha",JOptionPane.WARNING_MESSAGE);
            return;
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Error inesperado: ["+ex.getMessage()+"]","Error al cargar ficha",JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(abono <= saldo){
            hp = new HistorialPago(0, fecha,abono, 1,cboTipoPago.getSelectedIndex(),Integer.parseInt(txtFolioPagar.getText()));
            FnFicha cargar = new FnFicha();
            try {
                //                actualizar ficha
                ficha.setSaldo(ficha.getSaldo()-abono);
                //                cargar textbox con nuevos datos de la ficha
                if(ficha.getSaldo() < 0){
                    JOptionPane.showMessageDialog(null, "El monto del abono ingresado es incorrecto","Error al abonar",JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                int resp = JOptionPane.showConfirmDialog(null, "¿Esta seguro de realizar este abono?");
                if(resp==JOptionPane.YES_OPTION){

                    if(cargar.guardarAbono(hp)){
                        if(ficha.getSaldo()==0)
                        ficha.setEstado(2);
                        if(!cargar.actualizar(ficha)){
                            JOptionPane.showMessageDialog(null, "No se pudo modificar los datos de la ficha","Error",JOptionPane.INFORMATION_MESSAGE);
//                            limpiarTextField();
                            return;
                        }
//                        limpiarTextField();
                        cargarDatos(idFolio);
                        JOptionPane.showMessageDialog(null, "Abono registrado","Abonar",JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "Error al registrar abono","Abonar",JOptionPane.WARNING_MESSAGE);
                    }
                }else
                return;
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error desconocido: "+ex.getMessage(),"Abonar",JOptionPane.WARNING_MESSAGE);
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Error desconocido: "+ex.getMessage(),"Abonar",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Abono ingresado es mayor que el saldo registrado","No se pudo registrar abono", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarActionPerformed

