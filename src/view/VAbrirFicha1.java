    private int totalAbonos(int idFolio) throws SQLException, ClassNotFoundException {
        HistorialPagoDao hp = new HistorialPagoDao();
        int suma = 0;
        for (HistorialPago temp : hp.listar(idFolio)) {
            suma = suma + temp.getAbono();
        }
        return suma;
    }

    private void cargarHistorial(int idFicha) throws SQLException, ClassNotFoundException {
        DefaultTableModel modelo;
        modelo = new DefaultTableModel() {
           @Override
           public boolean isCellEditable(int fila, int columna) {
               return false; //Con esto conseguimos que la tabla no se pueda editar
           }
        };
        
        modelo.addColumn("Fecha");
        modelo.addColumn("Abono");
        modelo.addColumn("Tipo de Pago");
        tblListar.setModel(modelo);
        FnFicha load = new FnFicha(); 
        Ficha ficha = load.cargar(idFicha, 1);
        lblDetalleDespacho.setText(load.obtenerNombreDespacho(idFicha));
        lblTitulo.setText("Ficha numero: "+ficha.getId());
        lblCliente.setText("Cliente: "+ficha.getCliente().getNombre());
        TipoPago tp = null;
        int cont =0;
        try{
            for (HistorialPago temp : load.listarHistorial(idFicha)) {
                Object[] fila = new Object[3];
                fila[0] = temp.getFecha().toString();
                fila[1] = "$ "+temp.getAbono();
                tp = load.cargarTipoPago(temp.getIdTipoPago());
                if(tp!=null)
                    fila[2] = tp.getNombre();
                else
                    fila[2] = "No registrado.";
                modelo.addRow(fila);
            }
            tblListar.updateUI();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error, ["+e.getMessage()+"]");
        }
    }

