    private void btnGarantiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGarantiaActionPerformed
        jLabel1.setForeground(Color.BLACK);
        Date fecha = txtFecha.getDate();
        String lugar = txtEntrega.getText().toUpperCase();
        String hora = txtHora.getText();
        if(fecha==null){JOptionPane.showMessageDialog(null, "Debe seleccionar una nueva fecha de entrega","", JOptionPane.WARNING_MESSAGE);jLabel1.setForeground(Color.RED);return;}
        Date hoy=new Date();
        if(fecha.compareTo(hoy)<=0){JOptionPane.showMessageDialog(null, "La fecha debe ser mayor a la fecha de hoy","", JOptionPane.WARNING_MESSAGE);jLabel1.setForeground(Color.RED);return;}
        String id=this.idFolio.getText();
        String obs=txtObs.getText();
        FnFicha load=new FnFicha();
        
        
        try{
            int resp1 = JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea registrar una nueva garantía?","Confirmar",JOptionPane.YES_NO_OPTION);
            if(resp1 == JOptionPane.YES_OPTION){
                int resp2 = JOptionPane.showConfirmDialog(null, "Fecha: "+new SimpleDateFormat("dd-MM-yyyy").format(fecha)+", Hora: "+hora+", Lugar de entrega: "+lugar+"\n¿Los datos de la entrega son correctos?", "Confirmar datos", JOptionPane.YES_NO_OPTION);
                if(resp2 == JOptionPane.YES_OPTION){
                    if(load.garantia(obs,id,fecha,lugar,hora)){
                        JOptionPane.showMessageDialog(null,"Se ha generado una nueva garantía en el sistema.","Exito",JOptionPane.INFORMATION_MESSAGE);
                        return;
                    }
                }
            }
            JOptionPane.showMessageDialog(null,"No se pudo finalizar la operación","Operación cancelada",JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error:"+ex, "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGarantiaActionPerformed

