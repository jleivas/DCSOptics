    private void btnDespacharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDespacharActionPerformed
        int idSesion1 = GlobalValues.ID_USER;
        FnUser loadUser = new FnUser();
        try {
            User user = loadUser.cargar(idSesion1);
            if(user != null){
                if(user.getTipo()==3){
                    Date fecha = txtFechaDespacho1.getDate();
                    if(fecha==null){
                        JOptionPane.showMessageDialog(null, "Debe ingresar una fecha","Error",JOptionPane.INFORMATION_MESSAGE);
                        return;
                    }

                    FnFicha load2 = new FnFicha();
                    Ficha ficha = null;

                    if(chkRetiraCliente.isSelected()){
                        String rut = txtRut1.getText();
                        String nombre= txtNombre1.getText().toUpperCase();
                        String telefono=txtTelefono1.getText();
                        if(!txtTelefono2.getText().equals("")){
                            if(txtTelefono1.getText().equals("")){
                                telefono = txtTelefono2.getText();
                            }else{
                                telefono=txtTelefono1.getText() + " / " + txtTelefono2.getText();
                            }
                        }
                        String email = txtEmail1.getText();
                        String direccion = txtDireccion1.getText().toUpperCase();
                        String comuna = txtComuna1.getText().toUpperCase();
                        String ciudad = txtCiudad1.getText().toUpperCase();
                        int sexo = cboSexo1.getSelectedIndex();
                        int edad = (int) txtEdad1.getValue();
                        int estado = 1;

                        Cliente cliente = new Cliente(rut, nombre, telefono, email, direccion, comuna, ciudad, sexo, edad, estado);
                        FnCliente load = new FnCliente();

                        try {
                            ficha = load2.cargar(Integer.parseInt(lblIdFolio.getText()), 1);
                            rut = ficha.getCliente().getRut();
                            if(ficha.getEstado()!=2 && ficha.getEstado() != 4){
                                JOptionPane.showMessageDialog(null, "Solo se registran despachos a registros pagados","No se puede efectuar la operación",JOptionPane.INFORMATION_MESSAGE);
                                return;
                            }
                            if(fecha.compareTo(ficha.getFecha()) > 0 && fecha.compareTo(new Date()) <= 0){
                                if(load.modificar(cliente)){
                                    //                    guardar despacho en bd;
                                    Despacho despacho = new Despacho(0, rut, nombre, fecha, Integer.parseInt(lblIdFolio.getText()));

                                    load2.guardarDespacho(despacho);
                                    ficha.setDespacho(despacho);
                                    ficha.setEstado(3);
                                    load2.actualizar(ficha);
                                    cargarDatos(ficha.getId());
                                    JOptionPane.showMessageDialog(null,"Despacho registrado con datos de cliente actualizados.","Datos guardados", JOptionPane.INFORMATION_MESSAGE);
                                }else
                                JOptionPane.showMessageDialog(null, "No se pudo efectuar la operación", "Modificar Cliente", JOptionPane.WARNING_MESSAGE);
                            }else{
                                JOptionPane.showMessageDialog(null, "Debe ingresar una fecha válida","Error",JOptionPane.WARNING_MESSAGE);
                                return;
                            }
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(null, "Error inesperado "+ex.getMessage(), "Modificar Cliente", JOptionPane.WARNING_MESSAGE);
                        } catch (ClassNotFoundException ex) {
                            JOptionPane.showMessageDialog(null, "Error inesperado "+ex.getMessage(), "Modificar Cliente", JOptionPane.WARNING_MESSAGE);
                        }
                    }else{
                        String rut = txtRut1.getText();
                        FnValidaRut vr = new FnValidaRut();
                        if(!vr.validarRut(rut)){
                            JOptionPane.showMessageDialog(null, "El rut ingresado no existe.","Solicite cedula de identidad",JOptionPane.INFORMATION_MESSAGE);
                            return;
                        }
                        String nombre= txtNombre1.getText().toUpperCase();
                        String telefono = txtTelefono1.getText();
                        String email = txtEmail1.getText();
                        String direccion = txtDireccion1.getText().toUpperCase();
                        String comuna = txtComuna1.getText().toUpperCase();
                        String ciudad = txtCiudad1.getText().toUpperCase();
                        int sexo = cboSexo1.getSelectedIndex();
                        int edad = (int) txtEdad1.getValue();
                        int estado = 1;

                        Cliente cliente = new Cliente(rut, nombre, telefono, email, direccion, comuna, ciudad, sexo, edad, estado);
                        FnCliente load = new FnCliente();
                        try {
                            ficha = load2.cargar(Integer.parseInt(lblIdFolio.getText()), 1);
                            if(ficha.getEstado()!=2){
                                JOptionPane.showMessageDialog(null, "No se puede registrar despacho a registros pendientes","No está pagada",JOptionPane.INFORMATION_MESSAGE);
                                return;
                            }
                            if(fecha.compareTo(ficha.getFecha()) > 0 && fecha.compareTo(new Date()) <= 0){
                                if(load.guardarBoolean(cliente)){
                                    //                    guardar despacho en bd;
                                    Despacho despacho = new Despacho(0, rut, nombre, fecha, Integer.parseInt(lblIdFolio.getText()));

                                    if(!load2.guardarDespacho(despacho)){
                                        JOptionPane.showMessageDialog(null, "No se pudo insertar registro","Error",JOptionPane.WARNING_MESSAGE);
                                        return;
                                    }
                                    ficha.setDespacho(despacho);
                                    ficha.setEstado(3);
                                    load2.actualizar(ficha);
                                    cargarDatos(ficha.getId());
                                    JOptionPane.showMessageDialog(null,"Despacho registrado con nuevo cliente registrado.","Datos guardados", JOptionPane.INFORMATION_MESSAGE);
                                }else
                                JOptionPane.showMessageDialog(null, "No se pudo efectuar la operación", "Agregar Cliente", JOptionPane.WARNING_MESSAGE);
                                return;
                            }else{
                                JOptionPane.showMessageDialog(null, "Debe ingresar una fecha válida","Error",JOptionPane.WARNING_MESSAGE);
                                return;
                            }
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(null, "Error inesperado "+ex.getMessage(), "Agregar Cliente", JOptionPane.WARNING_MESSAGE);
                        } catch (ClassNotFoundException ex) {
                            JOptionPane.showMessageDialog(null, "Error inesperado "+ex.getMessage(), "Agregar Cliente", JOptionPane.WARNING_MESSAGE);
                        }

                    }
                    
                }else{
                    JOptionPane.showMessageDialog(null, "No tienes permisos suficientes para ejecutar esta operación.","No se puede efectuar la operación",JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Error: La sesión no se encuentra iniciada.","No se puede efectuar la operación",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Error inesperado: "+ex.getMessage(),"No se puede efectuar la operación",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnDespacharActionPerformed

